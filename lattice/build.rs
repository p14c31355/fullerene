//! Build script for Lattice.
//!
//! Generates `font8x16.bin` (95 printable‑ASCII glyphs × 16 bytes)
//! and places it in `OUT_DIR`.  `src/font.rs` picks it up via
//! `include_bytes!`.
//!
//! The `.bin` file is NOT tracked in Git — it is always regenerated.

use std::fs;
use std::path::PathBuf;

fn main() {
    let out_dir = PathBuf::from(std::env::var("OUT_DIR").unwrap());
    let bin_path = out_dir.join("font8x16.bin");

    // Write the 1520‑byte font binary.
    fs::write(&bin_path, &font_bytes()).expect("failed to write font8x16.bin");

    println!("cargo:rerun-if-changed=build.rs");
}

